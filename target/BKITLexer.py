class BKITLexer(Lexer):

    atn = ATNDeserializer().deserialize(serializedATN())

    decisionsToDFA = [ DFA(ds, i) for i, ds in enumerate(atn.decisionToState) ]

    ID = 1
    SEMI = 2
    COLON = 3
    VAR = 4
    KEY_WORD = 5
    OPERATOR = 6
    SEPARATOR = 7
    INT = 8
    FLOAT = 9
    BOOL = 10
    STRING = 11
    ARRAY = 12
    WS = 13
    ERROR_CHAR = 14
    ILLEGAL_ESCAPE = 15
    UNCLOSE_STRING = 16
    UNTERMINATED_COMMENT = 17

    channelNames = [ u"DEFAULT_TOKEN_CHANNEL", u"HIDDEN" ]

    modeNames = [ "DEFAULT_MODE" ]

    literalNames = [ "<INVALID>",
            "';'", "':'", "'Var'" ]

    symbolicNames = [ "<INVALID>",
            "ID", "SEMI", "COLON", "VAR", "KEY_WORD", "OPERATOR", "SEPARATOR", 
            "INT", "FLOAT", "BOOL", "STRING", "ARRAY", "WS", "ERROR_CHAR", 
            "ILLEGAL_ESCAPE", "UNCLOSE_STRING", "UNTERMINATED_COMMENT" ]

    ruleNames = [ "ID", "SEMI", "COLON", "VAR", "KEY_WORD", "OPERATOR", 
                  "SEPARATOR", "DEC", "HEX", "OCT", "INT", "DIGIT", "FLOAT", 
                  "BOOL", "STRING", "ELEMENT", "ARRAY", "WS", "ERROR_CHAR", 
                  "ILLEGAL_ESCAPE", "UNCLOSE_STRING", "UNTERMINATED_COMMENT" ]

    grammarFileName = "BKIT.g4"

    def __init__(self, input=None, output:TextIO = sys.stdout):
        super().__init__(input, output)
        self.checkVersion("4.8")
        self._interp = LexerATNSimulator(self, self.atn, self.decisionsToDFA, PredictionContextCache())
        self._actions = None
        self._predicates = None


    def emit(self):
        tk = self.type
        result = super().emit()
        if tk == self.UNCLOSE_STRING:       
            raise UncloseString(result.text[1:])
        elif tk == self.ILLEGAL_ESCAPE:
            raise IllegalEscape(result.text[1:])
        elif tk == self.ERROR_CHAR:
            raise ErrorToken(result.text)
        elif tk == self.UNTERMINATED_COMMENT:
            raise UnterminatedComment()
        elif tk == self.STRING:
            endPos = len(result.text) - 1
            result.text = result.text[1:endPos]
            return result
        else:
            return result;


